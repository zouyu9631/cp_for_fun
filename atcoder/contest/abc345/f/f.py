def solve():
    N, M, K = MII()
    g = [[] for _ in range(N)]
    for i in range(1, M + 1):
        u, v = GMI()
        g[u].append((v, i))
        g[v].append((u, i))

    if K & 1:
        print("No")
        return
    if K == 0:
        print("Yes")
        print(0)
        return

    res, cnt = [], 0
    seen = [False] * N
    light = [False] * N

    @bootstrap
    def dfs(u):
        nonlocal cnt
        seen[u] = True
        for v, i in g[u]:
            if seen[v]:
                continue
            yield dfs(v)
            if not light[v] and cnt < K:
                res.append(i)
                light[v] = True
                cnt += 1 + (-1 if light[u] else 1)
                light[u] = not light[u]
        yield None

    for u in range(N):
        if seen[u]:
            continue
        dfs(u)

    if cnt == K:
        print("Yes")
        print(len(res))
        print(*res)
    else:
        print("No")


