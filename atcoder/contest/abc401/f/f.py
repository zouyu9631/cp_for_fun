def solve():
    N1 = II()
    G1 = GRAPH(N1)
    N2 = II()
    G2 = GRAPH(N2)

    def bfs(g, s):
        n = len(g)
        dist = [-1] * n
        dq = deque()
        dq.append(s)
        dist[s] = 0
        while dq:
            u = dq.popleft()
            for v in g[u]:
                if dist[v] == -1:
                    dist[v] = dist[u] + 1
                    dq.append(v)
        return dist

    dist = bfs(G1, 0)
    S1 = max(range(N1), key=lambda i: dist[i])
    dist = bfs(G1, S1)
    T1 = max(range(N1), key=lambda i: dist[i])
    L1 = dist[T1]
    d1 = bfs(G1, T1)
    r1 = [max(dist[i], d1[i]) for i in range(N1)]

    dist = bfs(G2, 0)
    S2 = max(range(N2), key=lambda i: dist[i])
    dist = bfs(G2, S2)
    T2 = max(range(N2), key=lambda i: dist[i])
    L2 = dist[T2]
    d2 = bfs(G2, T2)
    r2 = [max(dist[i], d2[i]) for i in range(N2)]

    D = L1 if L1 >= L2 else L2

    r2.sort()
    P = [0] * (N2 + 1)
    for i in range(N2):
        P[i + 1] = P[i] + r2[i]

    more = 0

    for a in r1:
        t = D - a - 1
        pos = bisect_right(r2, t)
        cnt = N2 - pos
        if cnt <= 0:
            continue
        s = P[N2] - P[pos]
        more += cnt * (a + 1 - D) + s

    S = N1 * N2 * D + more
    print(S)


